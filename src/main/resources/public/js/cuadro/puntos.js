let agregar = function () {
    var wrapper = document.getElementById("puntos");
    wrapper.removeChild(document.getElementById("boton"));
    var elementos = document.createElement("div");
    elementos.className = "input-group align-items-center";
    var coordenadaX = document.createElement("div");
    coordenadaX.className = "col-sm-5";
    coordenadaX.style.paddingRight="0px";
    var elementoX = document.createElement("input");
    elementoX.className = "form-control";
    elementoX.type = "number";
    elementoX.step = "0.001";
    elementoX.placeholder = "Ingrese la longitud";
    elementoX.id = "puntoX";
    elementoX.required = true;
    coordenadaX.appendChild(elementoX);
    elementos.appendChild(coordenadaX);
    var coordenadaY = document.createElement("div");
    coordenadaY.className = "col-sm-5";
    coordenadaY.style.paddingLeft="0px";
    var elementoY = document.createElement("input");
    elementoY.className = "form-control";
    elementoY.type = "number";
    elementoY.step = "0.001";
    elementoY.placeholder = "Ingrese la latitud";
    elementoY.id = "puntoY";
    elementoY.required = true;
    coordenadaY.appendChild(elementoY);
    elementos.appendChild(coordenadaY);
    var adjunto = document.createElement("div");
    adjunto.className = "input-group-append";
    var eliminador = document.createElement("a");
    eliminador.id = "eliminador";
    eliminador.type = "button";
    eliminador.className = "btn btn-danger btn-sm";
    eliminador.onclick = (ev => {wrapper.removeChild(elementos);});
    var icono = document.createElement("i");
    icono.className = "fas fa-trash-alt";
    eliminador.appendChild(icono);
    adjunto.appendChild(eliminador);
    elementos.appendChild(adjunto);
    wrapper.appendChild(elementos);
    var boton = document.createElement("div");
    boton.id = "boton";
    boton.className = "col-10 input-group";
    boton.style.marginTop = "20px";
    var agregador = document.createElement("button");
    agregador.type = "button";
    agregador.className = "btn btn-outline-primary float-left";
    agregador.id = "agregador";
    agregador.onclick =  agregar;
    agregador.textContent = "Agregar";
    boton.appendChild(agregador)
    wrapper.appendChild(boton);
}